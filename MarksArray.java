class MarksArray {
    public static void main(String[] args) {

        // Array of 5 student marks
        int marks[] = {85, 90, 78, 88, 92};

        // Loop to print marks one by one
        for (int i = 0; i < marks.length; i++) {
            System.out.println("Marks of student " + (i + 1) + ": " + marks[i]);
        }
    }
}
